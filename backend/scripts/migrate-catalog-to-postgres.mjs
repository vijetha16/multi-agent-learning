import dotenv from "dotenv";
import mysql from "mysql2/promise";
import pg from "pg";

dotenv.config({path:".env.supabase.runtime.local",override:true});
const targetUrl=(process.env.POSTGRES_URL_NON_POOLING||process.env.POSTGRES_URL||"").replace(/[?&]sslmode=[^&]+/,"");
dotenv.config({path:".env",override:true});

const source=await mysql.createConnection({
  host:process.env.MYSQL_HOST||"127.0.0.1",port:Number(process.env.MYSQL_PORT||3306),
  user:process.env.MYSQL_USER||"root",password:process.env.MYSQL_PASSWORD||"",
  database:process.env.MYSQL_DATABASE||"lumio_learning",
});
if(!targetUrl)throw new Error("POSTGRES_URL is required");
const target=new pg.Client({connectionString:targetUrl,ssl:{rejectUnauthorized:false}});
await target.connect();

const tables=["users","interests","skills","courses","user_profiles","user_interests","user_skills",
  "course_levels","lessons","quizzes","quiz_questions","quiz_options","course_enrollments",
  "user_level_progress","user_lesson_progress","quiz_attempts","credit_transactions",
  "user_activities","badges","user_badges","certificates","refresh_tokens"];
const catalogTables=new Set(["interests","skills","courses","course_levels","lessons","quizzes","quiz_questions","quiz_options","badges"]);
const q=name=>`"${String(name).replaceAll('"','""')}"`;
function pgType(column){
  const type=column.DATA_TYPE;
  if(type==="bigint")return"BIGINT";if(type==="int"||type==="mediumint")return"INTEGER";
  if(type==="smallint"||type==="tinyint")return type==="tinyint"&&column.COLUMN_TYPE==="tinyint(1)"?"BOOLEAN":"SMALLINT";
  if(type==="decimal")return"NUMERIC";if(type==="float"||type==="double")return"DOUBLE PRECISION";
  if(type==="timestamp"||type==="datetime")return"TIMESTAMPTZ";if(type==="date")return"DATE";
  if(type==="json")return"JSONB";if(type==="boolean")return"BOOLEAN";
  if(type==="varchar"||type==="char")return`${type.toUpperCase()}(${column.CHARACTER_MAXIMUM_LENGTH})`;
  return"TEXT";
}
await target.query("BEGIN");
try{
  for(const table of [...tables].reverse())await target.query(`DROP TABLE IF EXISTS ${q(table)} CASCADE`);
  for(const table of tables){
    const [columns]=await source.query(
      `SELECT COLUMN_NAME,DATA_TYPE,COLUMN_TYPE,CHARACTER_MAXIMUM_LENGTH,IS_NULLABLE,COLUMN_DEFAULT,EXTRA,COLUMN_KEY
       FROM information_schema.COLUMNS WHERE TABLE_SCHEMA=? AND TABLE_NAME=? ORDER BY ORDINAL_POSITION`,
      [process.env.MYSQL_DATABASE||"lumio_learning",table]);
    const primaryColumns=columns.filter(column=>column.COLUMN_KEY==="PRI");
    const definitions=columns.map(column=>{
      const identity=String(column.EXTRA).includes("auto_increment")?" GENERATED BY DEFAULT AS IDENTITY":"";
      const nullable=column.IS_NULLABLE==="NO"?" NOT NULL":"";
      let fallback="";
      if(column.COLUMN_DEFAULT!==null&&!identity){
        const raw=String(column.COLUMN_DEFAULT);
        fallback=pgType(column)==="BOOLEAN"?" DEFAULT "+(raw==="1"?"TRUE":"FALSE"):
          /^current_timestamp/i.test(raw)?" DEFAULT CURRENT_TIMESTAMP":
          /^(true|false|\d+(\.\d+)?)$/i.test(raw)?` DEFAULT ${raw}`:` DEFAULT '${raw.replaceAll("'","''")}'`;
      }
      return`${q(column.COLUMN_NAME)} ${pgType(column)}${identity}${nullable}${fallback}${primaryColumns.length===1&&column.COLUMN_KEY==="PRI"?" PRIMARY KEY":""}`;
    });
    if(primaryColumns.length>1)definitions.push(`PRIMARY KEY (${primaryColumns.map(column=>q(column.COLUMN_NAME)).join(",")})`);
    await target.query(`CREATE TABLE ${q(table)} (${definitions.join(",")})`);
    const [indexes]=await source.query(
      `SELECT INDEX_NAME,GROUP_CONCAT(COLUMN_NAME ORDER BY SEQ_IN_INDEX) columns_list
       FROM information_schema.STATISTICS WHERE TABLE_SCHEMA=? AND TABLE_NAME=? AND NON_UNIQUE=0 AND INDEX_NAME<>'PRIMARY'
       GROUP BY INDEX_NAME`,[process.env.MYSQL_DATABASE||"lumio_learning",table]);
    for(const index of indexes)await target.query(
      `CREATE UNIQUE INDEX ${q(index.INDEX_NAME)} ON ${q(table)} (${String(index.columns_list).split(",").map(q).join(",")})`);
    if(catalogTables.has(table)){
      const [records]=await source.query(`SELECT * FROM \`${table}\``);
      for(const record of records){
        const names=Object.keys(record);const values=Object.values(record).map(value=>Buffer.isBuffer(value)?Boolean(value[0]):value);
        await target.query(`INSERT INTO ${q(table)} (${names.map(q).join(",")}) VALUES (${names.map((_,i)=>`$${i+1}`).join(",")})`,values);
      }
      const identity=columns.find(column=>String(column.EXTRA).includes("auto_increment"));
      if(identity)await target.query(`SELECT setval(pg_get_serial_sequence('${table}','${identity.COLUMN_NAME}'),COALESCE((SELECT MAX(${q(identity.COLUMN_NAME)}) FROM ${q(table)}),1),true)`);
    }
  }
  await target.query("COMMIT");
  console.log("Supabase schema created and catalog imported.");
}catch(error){await target.query("ROLLBACK");throw error}
finally{await source.end();await target.end()}
